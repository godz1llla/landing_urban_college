// ── Apply a language to the entire page ──────────────────────────
function applyLanguage(lang) {
    const dict = translations[lang];
    if (!dict) return;

    // Translate text nodes
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (dict[key] !== undefined) {
            el.textContent = dict[key];
        }
    });

    // Translate placeholders (inputs)
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (dict[key] !== undefined) {
            el.placeholder = dict[key];
        }
    });

    // Save preference
    localStorage.setItem('uc-lang', lang);
    document.documentElement.lang = lang;
}

// ── Boot ─────────────────────────────────────────────────────────
document.addEventListener('DOMContentLoaded', () => {
    const saved = localStorage.getItem('uc-lang') || 'kk';

    // Wire all pill buttons across all pill containers
    document.querySelectorAll('.lang-pill').forEach(pill => {
        pill.querySelectorAll('.lang-pill-btn').forEach(btn => {
            // Determine which language this button represents
            const label = btn.textContent.trim().toLowerCase();
            const langMap = { 'kaz': 'kk', 'рус': 'ru', 'eng': 'en', 'tur': 'tr' };
            btn.dataset.lang = langMap[label] || label;

            // Apply saved active state
            if (btn.dataset.lang === saved || (saved === 'kk' && !Object.values(langMap).includes('kk'))) {
                // Default active handled below
            }

            btn.addEventListener('click', () => {
                // Sync active class across ALL pill containers  
                document.querySelectorAll('.lang-pill-btn').forEach(b => {
                    b.classList.toggle('active', b.dataset.lang === btn.dataset.lang && b.textContent.trim() === btn.textContent.trim());
                });

                // Apply translation
                const validLangs = { 'ru': 'ru', 'en': 'en', 'tr': 'tr' };
                const targetLang = validLangs[btn.dataset.lang] || 'kk';
                applyLanguage(targetLang);
            });
        });
    });

    // Apply saved language on load
    const validLangs = ['ru', 'en', 'tr', 'kk'];
    applyLanguage(validLangs.includes(saved) ? saved : 'kk');
});
